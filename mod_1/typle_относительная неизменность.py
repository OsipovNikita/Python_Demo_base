t1 = (1,2, [30,40])
t2 = (1,2, [30,40])
print(t1==t2)       # True
print(id(t1[-1]))
t1[-1].append(50)   # (1, 2, [30, 40, 50])
print(t1)

print(t1==t2)       # False
print(id(t1[-1]))   # Изменилось только значение!

